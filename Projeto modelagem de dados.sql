-- TABELA CLIENTES
CREATE TABLE CLIENTES(
CNH VARCHAR2(100) NOT NULL,
NOME VARCHAR2(50) NOT NULL,
CARTAO INT NOT NULL,
TELEFONE VARCHAR (50) NOT NULL,
PRIMARY KEY (CNH)
);

-- TABELA AGENCIA
CREATE TABLE AGENCIA(
NUMERO_AG INT NOT NULL,
RUA VARCHAR2(100) NOT NULL,
CIDADE VARCHAR2(100) NOT NULL,
ESTADO VARCHAR2(100) NOT NULL,
CONTATO VARCHAR2(100) NOT NULL,
PRIMARY KEY (NUMERO_AG)
);

-- TABELA CARRO
CREATE TABLE CARRO(
PLACA VARCHAR2(100) NOT NULL,
MODELO VARCHAR2(50) NOT NULL,
NUMERO_AG INT NOT NULL,
ANO INT NOT NULL,
PRIMARY KEY (PLACA),
FOREIGN KEY(NUMERO_AG) REFERENCES AGENCIA(NUMERO_AG)
);

-- TABELA ALUGUEL
CREATE TABLE ALUGUEL(
DATA DATE,
CNH VARCHAR2(100) NOT NULL,
PLACA VARCHAR2(100) NOT NULL,
PRIMARY KEY (CNH,PLACA),
FOREIGN KEY(CNH) REFERENCES CLIENTES(CNH),
FOREIGN KEY(PLACA) REFERENCES CARRO(PLACA)
);


